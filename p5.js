// Given the days of the week in short format "Sun", "Mon" ... print in long format "Sunday", "Monday", ...
var day="Sun";
switch(day){
  case "Mon":
  console.log("Mon");
  break;
  case "Tue":
  console.log("Tue");
  break;
  case "Wed":
  console.log("Wed");
  break;
  case "Thu":
  console.log("Thu");
  break;
  case "Fri":
  console.log("Fri");
  break;
  case "Sat":
  console.log("Sat");
  break;
  case "Sun":
  console.log("Sun");
  break;
  default:
  console.log("No day");
}